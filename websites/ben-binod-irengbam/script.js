// Enhanced click effects for buttons
function addButtonEffects() {
    const buttons = document.querySelectorAll('.btn, .contact-btn');
    
    buttons.forEach(button => {
        button.addEventListener('click', function(e) {
            const ripple = document.createElement('span');
            const rect = this.getBoundingClientRect();
            const size = Math.max(rect.width, rect.height);
            const x = e.clientX - rect.left - size / 2;
            const y = e.clientY - rect.top - size / 2;
            
            ripple.style.cssText = `
                width: ${size}px;
                height: ${size}px;
                left: ${x}px;
                top: ${y}px;
                position: absolute;
                border-radius: 50%;
                background: rgba(255, 255, 255, 0.6);
                transform: scale(0);
                animation: ripple 0.6s linear;
                pointer-events: none;
            `;
            
            this.style.position = 'relative';
            this.style.overflow = 'hidden';
            this.appendChild(ripple);
            
            setTimeout(() => {
                ripple.remove();
            }, 600);
        });
    });
    
    // Add ripple animation
    const rippleStyle = document.createElement('style');
    rippleStyle.textContent = `
        @keyframes ripple {
            to {
                transform: scale(4);
                opacity: 0;
            }
        }
    `;
    document.head.appendChild(rippleStyle);
}

// Initialize all animations and interactions
function init() {
    // Core functionality
    handleNavbarScroll();
    updateActiveNavLink();
    animateTimeline();
    initScrollAnimations();
    
    // Visual enhancements
    enhanceFloatingIcons();
    createScrollToTopButton();
    addButtonEffects();
    
    // Trigger animations when sections come into view
    const skillsSection = document.querySelector('.skills');
    const heroSection = document.querySelector('.hero');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                if (entry.target === skillsSection) {
                    animateSkillBars();
                }
                if (entry.target === heroSection) {
                    setTimeout(animateCounters, 500);
                }
            }
        });
    }, {
        threshold: 0.3
    });
    
    observer.observe(skillsSection);
    observer.observe(heroSection);
}

// Wait for DOM to load
document.addEventListener('DOMContentLoaded', () => {
    // Minimal init: set skill bars instantly without animations
    document.querySelectorAll('.skill-progress').forEach(bar => {
        const skillLevel = bar.getAttribute('data-skill') || '0';
        bar.style.width = skillLevel + '%';
    });
});

