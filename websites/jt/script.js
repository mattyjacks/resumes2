// Add keyboard navigation support
document.addEventListener('keydown', function(e) {
    // ESC key closes mobile menu
    if (e.key === 'Escape') {
        const mobileMenu = document.getElementById('mobile-menu');
        const navMenu = document.getElementById('nav-menu');
        
        if (navMenu.classList.contains('active')) {
            mobileMenu.classList.remove('active');
            navMenu.classList.remove('active');
        }
    }
    
    // Arrow keys for page navigation (optional)
    if (e.altKey) {
        const currentPage = document.querySelector('.page.active');
        const allPages = Array.from(document.querySelectorAll('.page'));
        const currentIndex = allPages.indexOf(currentPage);
        
        if (e.key === 'ArrowLeft' && currentIndex > 0) {
            const prevPage = allPages[currentIndex - 1];
            showPage(prevPage.id);
        } else if (e.key === 'ArrowRight' && currentIndex < allPages.length - 1) {
            const nextPage = allPages[currentIndex + 1];
            showPage(nextPage.id);
        }
    }
});
